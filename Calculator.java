public class Calculator{
	// additional
	int sum(int num1, int num2){
		return num1+num2;
	}


	//multiplication
	int multiply(int num1, int num2){
		return num1*num2;
	}

	//substraction
	int substract(int num1, int num2){
		return num1-num2;
	}
	
	//divide by zero not allowed
	//division
	int divide(int num1, int num2){
		return num1/num2;
	}

}
